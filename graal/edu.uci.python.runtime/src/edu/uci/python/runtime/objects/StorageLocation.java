package edu.uci.python.runtime.objects;

/**
 * A storage location that abstracts the method for reading and writing values.
 */
public abstract class StorageLocation {

    private ObjectLayout objectLayout;

    protected StorageLocation(ObjectLayout objectLayout) {
        this.objectLayout = objectLayout;
    }

    public abstract boolean isSet(PythonBasicObject object);

    public abstract Object read(PythonBasicObject object);

    public abstract void write(PythonBasicObject object, Object value) throws GeneralizeStorageLocationException;

    public abstract Class getStoredClass();

    public ObjectLayout getObjectLayout() {
        return objectLayout;
    }

}
